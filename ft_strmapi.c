#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned int	y;
	char			*s1;

	i = 0;
	y = 0;
	while (s[y])
		y++;
	if (!(s1 = malloc(sizeof(char) * (y + 1))))
		return (NULL);
	while (s[i])
	{
		s1[i] = f(i, s[i]);
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
