#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int signe;
	int res;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	signe = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		signe *= (str[i] == '-') ? -1 : 1;
		i++;
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return ((res * signe));
}
