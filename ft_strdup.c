#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;
	int		y;

	y = 0;
	i = 0;
	while (src[y])
		y++;
	if (!(dest = malloc(sizeof(char) * y + 1)))
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
